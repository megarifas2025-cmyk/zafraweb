export function municipiosPorEstado(estado: string): string[] {
  const e = estado as EstadoRegistro;
  return MAP[e] ? [...MAP[e]].sort((a, b) => a.localeCompare(b, 'es')) : [];
}

function slugifyUbicacion(value: string): string {
  return value
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, ' ')
    .trim();
}

const ESTADO_ALIASES: Record<string, EstadoRegistro> = {
  'capital district': 'Distrito Capital',
  'distrito capital': 'Distrito Capital',
  'estado la guaira': 'Vargas',
  'la guaira': 'Vargas',
  vargas: 'Vargas',
};

export function normalizarEstadoVenezuela(estado: string | null | undefined): EstadoRegistro | null {
  const raw = typeof estado === 'string' ? estado.trim() : '';
  if (!raw) return null;
  const slug = slugifyUbicacion(raw);
  const alias = ESTADO_ALIASES[slug];
  if (alias) return alias;
  return ESTADOS_REGISTRO.find((item) => slugifyUbicacion(item) === slug) ?? null;
}

export function normalizarMunicipioVenezuela(
  estado: string | null | undefined,
  municipio: string | null | undefined,
): string | null {
  const estadoNorm = normalizarEstadoVenezuela(estado);
  const raw = typeof municipio === 'string' ? municipio.trim() : '';
  if (!raw) return null;
  if (!estadoNorm) return raw;
  const slug = slugifyUbicacion(
    raw
      .replace(/^municipio\s+/i, '')
      .replace(/^municipality\s+/i, ''),
  );
  const canon = municipiosPorEstado(estadoNorm).find((item) => slugifyUbicacion(item) === slug);
  return canon ?? raw;
}
